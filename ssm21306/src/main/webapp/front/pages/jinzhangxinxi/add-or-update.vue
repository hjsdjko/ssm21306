<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												bianhao: '',
																gonghao: '',
																qishouxingming: '',
																jine: '',
																beizhu: '',
																jinzhangshijian: '',
																userid: '',
												},
																																																																// 登陆用户信息
				user: {},
                                ro:{
                                   bianhao : false,
                                   gonghao : false,
                                   qishouxingming : false,
                                   jine : false,
                                   beizhu : false,
                                   jinzhangshijian : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
        		this.ruleForm.jinzhangshijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																
																																																			
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jinzhangxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='bianhao'){
					this.ruleForm.bianhao = obj[o];
					this.ro.bianhao = true;
					continue;
					}
					if(o=='gonghao'){
					this.ruleForm.gonghao = obj[o];
					this.ro.gonghao = true;
					continue;
					}
					if(o=='qishouxingming'){
					this.ruleForm.qishouxingming = obj[o];
					this.ro.qishouxingming = true;
					continue;
					}
					if(o=='jine'){
					this.ruleForm.jine = obj[o];
					this.ro.jine = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
					if(o=='jinzhangshijian'){
					this.ruleForm.jinzhangshijian = obj[o];
					this.ro.jinzhangshijian = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
				this.ruleForm.jine = 0;
				this.ro.jine = false;
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																
			// 多级联动参数
																																																
																																													
																																							// 日长控件选择日期时间
			jinzhangshijianConfirm(val) {
				console.log(val)
				this.ruleForm.jinzhangshijian = val.result;
				this.$forceUpdate();
			},
												
																																													
																																																																		
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																				var obj = uni.getStorageSync('crossObj');
				var table = uni.getStorageSync('crossTable');
				obj.jine = parseFloat(obj.jine) + parseFloat(this.ruleForm.jine)
				await this.$api.update(`${table}`, obj);
																																																																																																																																																								if((!this.ruleForm.jine)){
					this.$utils.msg(`金额不能为空`);
					return
				}
												if(this.ruleForm.jine&&(!this.$validate.isIntNumer(this.ruleForm.jine))){
					this.$utils.msg(`金额应输入整数`);
					return
				}
																																																																																																																																																if(this.ruleForm.id){
					await this.$api.update(`jinzhangxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`jinzhangxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

